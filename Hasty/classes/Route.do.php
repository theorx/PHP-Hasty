<?php

/**
 * This is Route data object / structure.
 */
class Route {

    /**
     * Base controller name.
     * @author Lauri Orgla
     * @version 1.0
     * @var string 
     */
    public $class;

    /**
     * Function name that will be called
     * @author Lauri Orgla
     * @version 1.0
     * @var string 
     */
    public $function;

    /**
     * Parameter for called function
     * @author Lauri Orgla
     * @version 1.0
     * @var string 
     */
    public $param;

}

?>